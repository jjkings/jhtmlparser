package org.kumasoft.html;

/**
 * This is the common character code definition.
 * @version $Revision: 1.1.1.1 $
 */
public interface CharCodes
{
    /**
     * The line feed.
     */
    public static final int LF = 0x0a;

    /**
     * The CR.
     */
    public static final int CR = 0x0d;

    /**
     * The TAB.
     */
    public static final int TAB = 0x09;

    /**
     * The white space.
     */
    public static final int SP = 0x20;
}
